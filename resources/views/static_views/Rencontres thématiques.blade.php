                <div class="panel-group" id="accordionTwoRight">
                <h2 class="soustitreprog" >1- Humanités numériques</h2>
<div class="soustitreprog" ><p> La digitalisation des ressources documentaires, la multiplication des bases de données, le traitement numérique des corpus ont profondément changé le paysage des sciences humaines et sociales. Bien qu’elles contribuent à désenclaver plusieurs régions comme le Maghreb, la place des humanités numériques pour les études sur ces régions, en plein bouleversement, est encore mal connue.

Cette rencontre évoquera, en invitant des acteurs européens et maghrébins, les stratégies de numérisation des bibliothèques, les nouvelles formations doctorales au numérique, le traitement informatique de la langue arabe, les nouvelles bases de données accessibles pour l’étude du Maghreb et du Moyen-Orient.
</p>
<h2 class="soustitreprog" >2- Traduction et circulations des savoirs
</h2>
<div class="soustitreprog"
><p> La circulation plurilingue des savoirs, en apparence facilitée par les transformations numériques du marché du livre, rencontre encore des obstacles dans des contextes où chaque langue, français, anglais, arabe, etc. a tendance à se développer d’une manière autonome.

La rencontre dressera un état des lieux de ces questions en réfléchissant, avec éditeurs et professionnels, à l’économie actuelle du livre dans la région et au métier de traducteur, à la diffusion des recherches en sciences humaines en arabe, turc, persan et autres langues, tout en évoquant des projets internationaux de traduction et de formation aux métiers de la traduction.

</p>

<h2 class="soustitreprog" >3- Sciences sociales et Société
</h2>
<div class="soustitreprog"
><p> Le contexte tunisien est particulièrement propice pour réfléchir aux relations entre universités et champ politique, dans le cadre de l’ouverture permise par les situations révolutionnaires de 2011. L’impact des transformations politiques et sociales sur les travaux et les institutions de recherche mais aussi le rôle que jouent les savants dans la cité seront évoqués à partir de plusieurs cas pratiques dans la région.

</p>
<h2 class="soustitreprog" >4- Arts, culture, cinéma

</h2>
<div class="soustitreprog"
><p> Adossée au programme culturel et notamment au festival du film, cette rencontre propose une réflexion sur les enjeux artistiques et culturels au Maghreb Moyen-Orient. Des ateliers thématiques aborderont les dimensions esthétiques des arts de la région, en évoquant aussi bien la calligraphie, le cinéma ou la musique, les enjeux éthiques, les conditions économiques du marché culturel et les implications politiques du travail des artistes.


</p>


