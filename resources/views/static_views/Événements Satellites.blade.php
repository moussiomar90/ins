<h2 class="sous1titreprog"> CRITÈRES DE PRÉSÉLECTION

</h2>

<p>Tous les étudiants des établissements universitaires co-organisateurs d'INSANIYYAT 2022 (l'Université de la Manouba, l'Université de Tunis, l'Université de Tunis El Manar et le Créative Digital Lab (CNCI) œuvrant également et non exclusivement en français sont les bienvenues pour postuler à ce concours.
</p>
<p> Les critères de présélection sont :</p>
</div>
<ul  style="padding-left: 10%; padding-bottom:3%">
  <li>Adéquation aux objectifs du concours</li>
  <li>Ambition et pertinence de l’innovation
</li>
