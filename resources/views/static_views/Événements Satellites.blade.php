Il prolonge un travail commun d’encadrement fait au cours de l’année, alliant  les impératifs d’interdisciplinarité et sensibilisant les étudiantEs à un usage et des échanges entre le français, l’arabe et l’anglais dans différents moments de la recherche et selon les capacités de chacunE.<br>
Les deux ateliers de lecture/écriture scientifique seront animés et modérés par
<strong>Khaoula Matri</strong>, <strong>Ramzi Ben Amara</strong>, <strong>Feyka Besbess</strong> et <strong>Maryem Ben Salem</strong>



</p>
<p> <strong>Atelier 1</strong> : Une rencontre entre les auteurs de l’ouvrage de <strong>L’Esprit de la Révolte </strong>et les étudiantEs, à la lumière des notices de l’ouvrage qui auront été lues et discutées en amont par les étudiantEs, au cours de l’année universitaire 2021/2022.
