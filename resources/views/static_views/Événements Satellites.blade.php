<h2 class="sous1titreprog" > I/ Bilan de fonctionnement, enjeux et perspectives
</h2>
<div class="sous1titreprog"
><p>
Créé en 2014, ce centre est le produit d’un collectif de chercheur-e-s rattachés à des institutions universitaires locales et européennes, œuvrant à décloisonner les disciplines avec une orientation vers l’Afrique en favorisant les problématiques fraîches traversées par la société tunisienne dans son ancrage maghrébin, méditerranéen et africain.

<br> Deux axes de présentation du Centre : Durée : 2 heures


</p>

<h2 class="sous1titreprog" > - Un état des lieux de la production du savoir anthropologique depuis un pays du Sud :
</h2><p>
formation des premières générations dans l’université tunisienne et la production de l’anthropologie chez soi en mettant l’accent sur les modalités de partenariat et les animations scientifiques.



</p>
<h2 class="sous1titreprog" > - Premières recherches sur les études africaines depuis la Tunisie
</h2><p>
présenté par quatre étudiant-e-s de Master Etudes Africaines

</p>
<h2 class="sous1titreprog" > Axe 1 : Animé par Ramzi Ben Amara et Khaoula Matri, enseignantEs chercheurEs : 20 mn
</h2><p>
Un panorama des activités du centre d’anthropologie depuis sa création :
Ramzi Ben Amara : Les modules d’enseignement (Licence et Master) et les institutions de recherches et universitaires partenaires
Khaoula Matri : L’animation scientifique : Activités et événements (conférences, journées d’études et de formation, ateliers, etc.), en mettant l’accent sur les perspectives de dialogue avec les autres chercheur-e-s de différentes universités locales et internationales et l’impact de cette animation sur la visibilité du centre et la formation des étudiante-e-s.
Présentation des publications : deux numéros publiés + livraisons en cours

</p>
<h2 class="sous1titreprog" > Axe 2 : Présenté par quatre étudiantEs
</h2><p>
Comment surmonter les contraintes financières et méthodologiques ? En l’absence de financement pour des stages de terrain dans des pays subsahariens, les quatre interventions proposent de présenter les difficultés de terrain et la manière de les contourner en choisissant des corpus différents, en abordant des problématiques d’actualité et en renouvelant les méthodes.<br>

<strong>Asma Belhaj Hassan </strong>: L’expérience migratoire des travailleuses subsahariennes en Tunisie après 2011 <br>
<strong>Rahma Talmoudi </strong>: Les représentations de l’Afrique et des africains dans les manuels scolaires du troisième degré de l'enseignement de base en Tunisie
<strong>Abir Menci</strong> : Représentations et vécus des mères célibataires en Tunisie<br>
<strong>Houssem Boujarra </strong>: Les mouvements contestataires en Afrique. Les cas de Menich Msemah  [2015-2017] en Tunisie et Balai Citoyen  [2013-2015] au Burkina Faso.


