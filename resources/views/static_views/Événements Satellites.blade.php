                <h2 class="soustitreprog" > |  @insaniyyat : DIGINNEX
</h2>
<div class="soustitreprog" >

<p> <strong>La conception et le développement d'expériences digitales innovantes en temps de crises (écologique, agricole, économique, sociale, sanitaire, politique, etc.).</strong> <br>

Dans le cadre d’INSANIYYAT 2022 aura lieu le concours interuniversitaire des meilleures avancées des expériences digitales innovantes : le Prix DigInnEx. Ce concours est lancé par l’Association du Multimédia et de l’Audiovisuel AMAVI et organisé en partenariat avec Orange et l'Institut des Arts Multimédia de la Manouba ISAMM. <br>
Les retombés de ce concours se veulent bénéfiques pour le milieu de l’innovation pédagogique et de son avancé au service de l’apprenant, de l’apprentissage, de l’employabilité et de l’essor d’une société et d’une économie basée sur le savoir et ouverte aux opérateurs des industries multisectorielles (culturelles, agricoles et alimentaire, informatique, ..),  et créatives. Cette compétition sera le début d’un effort concerté entre universités, organisations et entreprises pour mettre l’apprenant et l’atteinte de ses objectifs d’apprentissages au centre de nos conceptions de projets axés sur la conception et le développement d'expériences digitales innovantes en temps de crises (écologique, agricole, économique, sociale, sanitaire, politique, etc.).


</p>
<h2 class="sous1titreprog" > ORGANISATEURS

</h2>
<p> L’Association du Multimédia et de l’Audiovisuel (AMAVI) regroupe des universitaires, des enseignants chercheurs et des étudiants interdisciplinaires. Créée il y a près de 15 ans, elle est l’une des plus actives associations d’établissements d’enseignement supérieur et de recherche de l'UMA. Dans le respect de la diversité des disciplines, des cultures et des langues, AMAVI agit pour un rayonnement universitaire engagé dans le développement social, culturel et technologique des étudiants et des enseignants chercheurs.  Elle accompagne les établissements d’enseignement supérieur et de recherche pour relever 3 grands défis : (i) l’implication dans le développement global de la société, (ii) la recherche et l'innovation culturelle et numérique et  (iii) l’insertion professionnelle et l’employabilité des diplômés.

 L'Institut des Arts Multimédia de la Manouba (ISAMM ) est la première institution d'enseignement supérieur public à dispenser des enseignements dans le cinéma, l'audiovisuel, le multimédia dans ses modalités techniques et artistiques. Crée en l'an 2000, l'ISAMM a pour principale mission de former les étudiants à des parcours de formation spécialisés et professionnels et de les doter des compétences nécessaires pour une meilleure insertion professionnelle. L'ISAMM se démarque grâce à son savoir-faire en simulation, ludification et en réalité augmentée pour n’en nommer que trois.

 ORANGE Tunisie compte plus de 1200 collaborateurs au service de plus de 4 millions de clients sur quasiment l'ensemble du territoire. Grace à la fois à son savoir-faire et à son innovation technologique, Orange Tunisie a pour ambition de devenir l'opérateur tunisien préféré, tout en maintenant une croissance équilibrée et créatrice de valeur pour tous. Dans cette vision stratégique de croissance partagée, Orange Tunisie a décidé de déployer une démarche d’opérateur responsable, humain et solidaire en mettant la technologie et l'innovation numérique au profit du développement socioéconomique.
 Notre ambition est d’être le principal acteur qui promeut l’innovation numérique inclusive et qui met le numérique au service de l’employabilité et de l’entrepreneuriat. Lancé en 2019, Orange Digital Center est un nouveau concept déployé par le groupe Orange dans la zone Afrique et Moyen Orient qui a pour ambition de promouvoir la formation numérique et soutenir l’employabilité des jeunes, les préparer aux emplois de demain et pour encourager l’innovation et l’entrepreneuriat technologique.
 Ce centre réunit dans un même lieu un ensemble de programmes tous gratuits allant de la formation des jeunes au numérique jusqu’à l’accélération de start-up en passant par l’accompagnement des porteurs de projets et l’investissement dans ces derniers. Concrètement, le centre comprend une école du code, un FabLab solidaire, un accélérateur de start-up « Orange Fab » et Orange Digital Ventures Africa, le fonds d’investissement du Groupe Orange.
 Ces quatre programmes sont intégrés au sein d’un même espace pour former un véritable tiers lieu de l’innovation. Chacun des programmes de Orange Digital Center s’adresse à un public spécifique. L’école du code, centre technologique libre d’accès et totalement gratuit, s’adresse particulièrement aux étudiants, aux jeunes développeurs et porteurs de projets. Le FabLab solidaire, atelier de fabrication numérique pour créer et prototyper avec des équipements numériques, s’adresse aussi à ce type de public, mais cible également des jeunes sans diplôme et sans emploi.
 Orange Fab et Orange Digital Ventures Africa sont des programmes qui ciblent, quant à eux, les entrepreneurs. Le premier est un accélérateur de start-up qui accompagne les plus prometteuses dans leur développement commercial et les aide à construire des partenariats commerciaux avec le Groupe Orange et le réseau mondial des Orange Fabs. Le second, Orange Digital Ventures Africa, est un fonds d’investissement, doté de 50 millions d’euros qui finance les start-up innovantes des pays du continent africain.

 </p>
