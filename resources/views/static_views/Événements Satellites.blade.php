<h2 class="sous1titreprog"> CRITÈRES DE PRÉSÉLECTION

</h2>

<p>Tous les étudiants des établissements universitaires co-organisateurs d'INSANIYYAT 2022 (l'Université de la Manouba, l'Université de Tunis, l'Université de Tunis El Manar et le Créative Digital Lab (CNCI) œuvrant également et non exclusivement en français sont les bienvenues pour postuler à ce concours.
</p>
<p> Les critères de présélection sont :</p>
</div>
<ul  style="padding-left: 10%; padding-bottom:3%">
  <li>Adéquation aux objectifs du concours</li>
  <li>Ambition et pertinence de l’innovation
</li>
  <li>Valeur d’usage, ambition, marché et maturité du projet
</li>
  <li> Crédibilité de l’équipe
</li>

</ul>
<div  class="soustitreprog">
<h2 class="sous1titreprog"> TROIS CATÉGORIES</h2>

<p>La pierre angulaire de l'innovation digitale en temps de crises (écologique, agricole, économique, sociale, sanitaire, politique, etc.) est composée de plusieurs facettes. Les Prix DigInnEx se concrétiseront en 3 distinctions spécifiques reliées aux apprentissages effectués via des technologies. Pour chacune des catégories présentées ci-dessous, il faudra se distinguer en démontrant l’application d’un ou de plusieurs critères techno-pédagogiques. Les détails sur le processus d’évaluation se trouve à la section s’y afférent. Mais il est important de noter que même si chaque projet sera évalué sur plusieurs critères correspondant à sa distinction, seul le critère ayant accumulé le plus de points sera retenu pour déterminer le score du projet.
</p>
</h2>
</div>

<div  class="soustitreprog">
<h2 class="sous1titreprog">Distinction DigInnEx 1 : Meilleure application du ludique au service des apprenant.e.s</h2>
</h2>
<p>L’application d’éléments de jeux vidéo au monde de l’apprentissage numérique dans le but de proposer des solutions innovantes permettant d'enrichir l’expérience des apprenant.e.s en temps de crises .
Cela pourrait inclure, sans s’y limiter, à se démarquer à travers l’utilisation calculée d’avatars, d’une trame narrative, de badges, de systèmes de classement, d’une interdisciplinarité entre plusieurs concepts ou de l’intégration de méthodes d’interaction et de pratique qui suscitent de nouvelles aptitudes ou encore d’autres procédés qui encouragent l’assimilation des connaissances par l’application, la pratique, la collaboration ou la compétition positive.
</p>

</div>


<div  class="soustitreprog">

<h2 class="sous1titreprog">Distinction DigInnEx 2 : Meilleure innovation dans l’utilisation des technologies pour développer un nouveau produit ou service
</h2>

<p>Introduction d’une nouvelle technologie ou d’une méthode de conception technologique nouvelle qui pourrait ouvrir d’importantes opportunités de création de valeur et de différenciation créative, tant pour les acteurs universitaires que pour des startups et nouveaux partenaires de différents secteurs.
Cela pourrait inclure, sans s’y limiter, à se démarquer à travers l’utilisation calculée d’une nouvelle technologie démontrant des innovations fortes, avec une ambition de marché élevée, des innovations relevant de l’expérience utilisateur, du modèle d’affaires ou de l’organisation ou des innovations technologiques comme le recours à l’intelligence artificielle, le big data, le cloud computing, la réalité augmentée ou les objets connectés.



</p>

</div>



<div  class="soustitreprog">

<h2 class="sous1titreprog">Distinction DigInnEx 3 : Prix spécial du Jury

</h2>

<p>Le prix spécial du jury souligne la singularité d'un projet innovant dont le travail a été jugé remarquable. Il récompense une expérience digitale méritant d’être distinguée, mais n’entrant possiblement pas dans aucune des autres catégories de prix.


Cela pourrait inclure sans s’y limiter, à se démarquer à travers plusieurs des catégories du concours ou à travers aucune des catégories du concours puisque l’innovation proposée se distingue par une large couverture intersectorielle.




</p>

</div>

