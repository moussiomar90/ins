                            <p > <b>​​Insaniyyat est un forum international des sciences sociales et des humanités </b>réunissant des chercheurs, intellectuels et artistes du 20 au 24 septembre 2022 à Tunis. Première manifestation scientifique de cette dimension internationale en Tunisie, il a pour but d’offrir un panorama des recherches en sciences humaines et sociales sur les régions dont le pays est un carrefour (Maghreb-Moyen-Orient, Europe et Afrique), tout en favorisant la rencontre d’intellectuels et des femmes et hommes de culture du monde entier.

Le Forum s’ouvre à toutes les dimensions des sciences humaines dans leurs relations avec la société, de la sociologie à l’urbanisme, de la littérature à l’architecture, de l’histoire au design. <br> <p>Ancré dans le contexte tunisien et maghrébin actuel, il entend défendre la place des sciences humaines et sociales dans la vie de la cité, tout en réfléchissant aux origines et aux impacts des crises sanitaires, mais aussi politiques et économiques, sur leur évolution récente. Le Forum entend réagir aux cloisonnements entre traditions académiques, entre générations comme aux barrières linguistiques et logistiques entre les bibliographies et les outils de références.
<br><br>

</p>


